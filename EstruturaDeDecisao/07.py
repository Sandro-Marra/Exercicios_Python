# Faça um Programa que leia três números e mostre o maior e o menor deles.

numero1 = float(input('Informe um número: '))
numero2 = float(input('Informe um número: '))
numero3 = float(input('Informe um número: '))

print('o maior número é', max(numero1, numero2, numero3))
print('o menor número é', min(numero1, numero2, numero3))


